{
    // sum of squares: 
    n = 10
    sum = 0
    for (let i = 0; i <= n; i++) {
        sum += i * i
        console.log(`square of ${i} = ${i * i} and sum = ${sum}`)
    }

    console.log(`Sum of ${n} numbers is ${sum}`)
}

{
    // sum of cubes: 
    n = 10
    sum = 0
    for (let i = 0; i <= n; i++) {
        sum += i * i * i
        console.log(`cube of ${i} = ${i * i * i} and sum = ${sum}`)
    }

    console.log(`Sum of ${n} numbers is ${sum}`)
}